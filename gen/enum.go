package main

import (
  "fmt"
  "sort"
  "os"
  "log"
)

func genEnums(enums map[string]string) {
  lines := make([]string, 0)
  for name, module := range enums {
    lines = append(lines, fmt.Sprintf("  %s = C.%s%s\n", name, module, name))
  }
  sort.Strings(lines)
  outputFile, err := os.Create("../enum.go")
  if err != nil { log.Fatal(err) }
  outputFile.Write([]byte(`package elm // generated by gen/enum.go
//#include <Elementary.h>
//#include <Emotion.h>
//#include <Eio.h>
import "C"
const (
`))
  for _, l := range lines {
    outputFile.Write([]byte(l))
  }
  outputFile.Write([]byte(")\n"))
  outputFile.Close()
}
